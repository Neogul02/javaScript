Math.ceil(1.2); // 2
Math.ceil(-0.9); // -0
Math.ceil(4);   // 4
// Math.ceil() 함수는 주어진 숫자보다 크거나 같은 숫자 중 가장 작은 숫자를 integer 로 반환합니다.

Math.floor(5.9); // 5
Math.floor(5);   // 5
Math.floor(-5.05); // -6
// Math.floor() 함수는 주어진 숫자와 같거나 작은 정수 중에서 "가장 큰 수"를 반환합니다.

Math.random();
// Math.random() 함수는 이상 1 미만의 부동소숫점 의사 "난수"를 반환합니다.

Math.round(0.9); // 1
Math.round(5.5); // 6
Math.round(-5.05) // -5
//Math.round() 함수는 입력값을 반올림한 수와 가장 가까운 "정수" 값을 반환합니다

Math.pow(7,3); // 343
// Math.pow() 함수는 base(밑)^exponent(지수)처럼 base 에 exponent를 제곱한 값을 반환합니다.

Math.max(1,2,3,4,5); // 5
Math.max(-1,-3,-5); // -1
// Math.max()함수는 입력값으로 받은 0개 이상의 숫자 중 가장 큰 숫자를 반환합니다.

Math.min(1,2,3,4,5); // 1
Math.min(-1,-3,-5); // -5 
// Math.min() 함수는 주어진 숫자들 중 가장 작은 값을 반환합니다.